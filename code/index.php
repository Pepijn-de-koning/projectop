              Als mediadeveloper programmeer en onderhoud je
              (mobiele) websites, apps en webapplicaties. Je
              leert niet alleen veel over technologie en
              programmeren, maar ook over de basis van digitale
              vormgeving. Want een product moet niet alleen
              goed werken, ook het idee erachter moet goed
              zijn en het product moet er aantrekkelijk uitzien.
              Ook leer je samenwerken met mediavormgevers,
              want zij nemen later de vormgeving voor hun rekening.
              Je opleiding begint met een periode waarin je samen met
              studenten van de opleiding Gamedeveloper de lessen
              volgt. Na deze periode kies je ervoor om verder te
              studeren als mediadeveloper.
          </p>
